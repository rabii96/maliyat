     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        if($request->ajax()) {
            $taskName = $request->input('taskName');
            if($taskName!=''){
                $task = new Task;
                $task->name = $taskName;
                $task->save();
                return($task);
            }else{
                return('Empty task name');
            }     
        }
    }

